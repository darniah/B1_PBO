package pertemuan2.model;

public class Fakultas {
    public String nama;
    public String kode;
    public String id;

    public Fakultas() {
        this.nama = "";
        this.kode = "";
        this.id = "";
    }

    public Fakultas(String nama, String kode, String id) {
        this.nama = nama;
        this.kode = kode;
        this.id = id;
    }


}
